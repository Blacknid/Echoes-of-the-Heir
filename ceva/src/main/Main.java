package main;

/**
 * Static app state + pre-launch bootstrap. No longer owns a Swing window (libGDX owns the
 * window via the lwjgl3 DesktopLauncher → MichiGame). {@link #bootstrap()} runs the update
 * check + license load/watchdog before the GL window opens; the desktop launcher calls it.
 */
public class Main {

    public static final boolean OFFLINE_MODE = false;
    public static final boolean DEBUG_MODE = true;
    public static volatile String LICENSE_KEY = null;  // Generated by installer → license.properties

    /**
     * Called by LicenseManager's watchdog the moment it detects on-disk tampering, file swap, or
     * machine-fingerprint drift. Nulling this key forces subsequent cloud-save / multiplayer
     * connect attempts to abort cleanly.
     */
    public static void invalidateLicense() {
        LICENSE_KEY = null;
    }

    /**
     * Pre-launch bootstrap (update check + license). Returns false if the process should exit
     * before opening the window (a required update was downloaded / declined). The desktop
     * launcher calls this, then starts the libGDX application.
     */
    public static boolean bootstrap() {
        // Mandatory update check — must run before the window/game so the JAR can be replaced.
        if (!update.UpdateClient.checkAndApply()) {
            return false;
        }

        // Load + verify the signed, machine-bound license (null on any failure — game still runs).
        LICENSE_KEY = data.LicenseManager.load();
        if (LICENSE_KEY != null) {
            data.LicenseManager.startWatchdog(60);
        }

        // Dev mode: let ResourceCache read .tmx/.tsx files directly from src/res so in-game R
        // reload picks up Tiled edits without a resource sync step.
        if (DEBUG_MODE) {
            util.ResourceCache.setDevSourcePath(System.getProperty("user.dir") + "/ceva/src");
        }
        return true;
    }
}
